using UnityEngine;

/// <summary>
/// 给GameObject添加标签，方便记录一些额外信息
/// </summary>
public class Tag : MonoBehaviour
{
    /// <summary>
    /// 存储float变量
    /// </summary>
    public float floatValue;
    /// <summary>
    /// 存储int变量
    /// </summary>
    public int intValue;
    /// <summary>
    /// 存储bool变量
    /// </summary>
    public bool boolValue;
    /// <summary>
    /// 存储string变量
    /// </summary>
    public string stringValue;
    /// <summary>
    /// 存储UnityEngine.Object变量
    /// </summary>
    public UnityEngine.Object UnityObjectValue;
    /// <summary>
    /// 存储System.Object变量
    /// </summary>
    public System.Object SystemObjectValue;
}
